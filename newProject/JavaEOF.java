package newProject;

import java.util.Scanner;

public class JavaEOF {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
        int n = 0;
        while(n<3) {
        	n++;
        
        	System.out.println(n);
        	System.out.println(n + " " + sc.nextLine());
        }
        
	}

}
